/* ── Helpers ── */
export function getInterviewCategoryWithQuestions(categoryId) {
  const cat = INTERVIEW_CATEGORIES.find(c => c.id === categoryId)
  if (!cat) return null
  const questions = cat.questions.map(slug => ({ ...QUESTIONS_DATA[slug], slug }))
  return { ...cat, questions }
}

export function getQuestionBySlug(slug) {
  const q = QUESTIONS_DATA[slug]
  if (!q) return null
  const category = INTERVIEW_CATEGORIES.find(c => c.questions.includes(slug))
  return { ...q, slug, categoryData: category }
}
